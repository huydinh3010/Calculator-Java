    public void activeButton(boolean status,String...list){
        for (String listAt : list) {
            switch (listAt) {
                case "+": jButtonCong.setEnabled(status); break;
                case "-": jButtonTru.setEnabled(status); break;
                case "x": jButtonNhan.setEnabled(status); break;
                case "/": jButtonChia.setEnabled(status); break;
                case "0": jButtonNum0.setEnabled(status); break;
                case "1": jButtonNum1.setEnabled(status); break;
                case "2": jButtonNum2.setEnabled(status); break;
                case "3": jButtonNum3.setEnabled(status); break;
                case "4": jButtonNum4.setEnabled(status); break;
                case "5": jButtonNum5.setEnabled(status); break;
                case "6": jButtonNum6.setEnabled(status); break;
                case "7": jButtonNum7.setEnabled(status); break;
                case "8": jButtonNum8.setEnabled(status); break;
                case "9": jButtonNum9.setEnabled(status); break;
                case ".": jButtonDot.setEnabled(status); break;
                case "+/-": jButtonDaoDau.setEnabled(status); break;
                case "1/x": jButtonNghichDao.setEnabled(status); break;
                case "%": jButtonPhanTram.setEnabled(status); break;
                case "âˆš": jButtonSqrt.setEnabled(status); break;
                case "MC": jButtonMC.setEnabled(status); break;
                case "MR": jButtonMR.setEnabled(status); break;
                case "M+": jButtonMCong.setEnabled(status); break;
                case "M-": jButtonMTru.setEnabled(status); break;
                case "=": jButtonBang.setEnabled(status); break;
                case "C": jButtonC.setEnabled(status); break;
            }
        }
    }
    
